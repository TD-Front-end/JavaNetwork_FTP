    private void btDangkyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDangkyActionPerformed
            // TODO add your handling code here:          
        String username=txtUsename.getText(); 
        String password=new String(txtPassword.getPassword()); 
        String cfPassword=new String(txtcpassword.getPassword());
        int quyen = 0;
        if(cboread.isSelected()) {
            quyen=0; 
        } else if (cbowrite.isSelected()) {
            quyen=1; 
        } else if(cbofull.isSelected())
            quyen=2;
        if(!password.equals(cfPassword))
        {
            JOptionPane.showMessageDialog(null, "Mật khẩu bị lỗi!");
        }else{
            try {
                //lấy đường dẫn của project hiện tại
                File directory = new File("");
                String path = txtDuongdan.getText();
                if(path.equals(""))
                {
                    //ko nhập đường dẫn sẽ tạo ra thư mục
                    path = directory.getCanonicalPath()+"\\"+txtUsename.getText();
                    (new File(path)).mkdir();
                    path = path.replace("\\", "/");
                }else{
                    path=path.replace("\\", "/");
                    System.out.print(path);
                    //Tag ra kết nối và thêm User vào CSDL
                    DBAccess acc=new DBAccess();
                    String query="insert into taikhoan (username,password,Duongdan,Quyentruyxuat) values('"+username+"','"+password+"','" + path + "',"+quyen + ")";
                    System.out.println(query);
                    if (acc.Update(query) > 0) {
                        JOptionPane.showMessageDialog(null, "Đăng ký thành công!");
                        xoa();
                    } else {//Đăng ký thất bại
                        JOptionPane.showMessageDialog(null, "Đăng ký thất bại!");
                    }                    
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,e.toString());
            }
        }        
    }//GEN-LAST:event_btDangkyActionPerformed

