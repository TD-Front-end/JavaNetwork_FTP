    private void btnTimduyetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimduyetActionPerformed
        // tìm duyệt đường dẫn
        this.FileChooserServer.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        if(this.FileChooserServer.showOpenDialog(this) == JFileChooser.APPROVE_OPTION){
            try {
                //lấy đường dẫn 
                String path = this.FileChooserServer.getSelectedFile().getCanonicalPath();
                txtDuongdan.setText(path);
                this.validate();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Lỗi đường dẫn!");
            }
        }
        
    }//GEN-LAST:event_btnTimduyetActionPerformed

